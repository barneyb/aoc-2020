    private static long getPartTwo(List<Block> blocks) {
        Map<String, String> memory = new HashMap<>();
        for (Block b : blocks) {
            for (Write w : b.writes) {
                for (String addr : applyAddressMask(b.mask, w.addr)) {
                    memory.put(addr, w.value);
                }
            }
        }
        return memory.values().stream()
                .map(it -> Long.valueOf(it, 2))
                .reduce(0L, Long::sum);
    }

    private static Collection<String> applyAddressMask(String mask, String addr) {
        StringBuilder sb = new StringBuilder(36);
        for (int i = 0, l = mask.length(); i < l; i++) {
            char c = mask.charAt(i);
            if (c == '0') {
                sb.append(addr.charAt(i));
            } else {
                sb.append(c);
            }
        }
        Queue<String> queue = new LinkedList<>();
        queue.add(sb.toString());
        List<String> result = new ArrayList<>();
        while (!queue.isEmpty()) {
            String s = queue.remove();
            int i = s.indexOf('X');
            if (i < 0) {
                result.add(s);
                continue;
            }
            queue.add(s.substring(0, i) + '0' + s.substring(i + 1));
            queue.add(s.substring(0, i) + '1' + s.substring(i + 1));
        }

        return result;
    }

    private static long getPartOne(List<Block> blocks) {
        Map<String, String> memory = new HashMap<>();
        for (Block b : blocks) {
            for (Write w : b.writes) {
                memory.put(w.addr, applyValueMask(b.mask, w.value));
            }
        }
        return memory.values().stream()
                .map(it -> Long.valueOf(it, 2))
                .reduce(0L, Long::sum);
    }

    private static String applyValueMask(String mask, String value) {
        assert mask.length() == value.length();
        StringBuilder sb = new StringBuilder(36);
        for (int i = 0, l = mask.length(); i < l; i++) {
            char c = mask.charAt(i);
            sb.append(c == 'X' ? value.charAt(i) : c);
        }
        return sb.toString();
    }

